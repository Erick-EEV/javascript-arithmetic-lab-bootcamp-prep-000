function add(a, b){
  return a + b
}
function subtract(a, b){
  return a - b
}
function multiply(a, b){
  return a * b
}
function divide(a, b){
return a / b
}
function increment(n){
  return (n + 1) 
}
function decrement(n){
  return (n - 1)
}
function makeInt(n){
  parseInt(10 + n)
  return n 
}
function preserveDecimal(n){
  parseFloat(n)
  return n
}